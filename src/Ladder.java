public class Ladder {

  private int numOfPeople;
  private int height;

  public Ladder(int numOfPeople, int height) {
    this.numOfPeople = numOfPeople;
    this.height = height;
  }

  public int getNumOfPeople() {
    return numOfPeople;
  }

  public int getHeight() {
    return height;
  }
}
